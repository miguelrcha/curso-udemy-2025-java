public class ConditionalForTwo {
    public static void main(String[] args) {
        for(int i = 0; i < 5; i++){
            System.out.println("Casa "+ i + " decimal");
        }
        System.out.println("\n");
        for(int i = 4; i >= 0; i--){
            System.out.println("Casa "+ i + " decimal");
        }
    }
}
